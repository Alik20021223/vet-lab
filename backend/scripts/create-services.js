async function createServices() {
  try {
    console.log('Начинаем создание услуг...\n');

    for (const serviceData of services) {
      // Проверяем, существует ли уже услуга с таким названием
      const existing = await prisma.service.findFirst({
        where: { title: serviceData.title },
      });

      if (existing) {
        console.log(`⚠️  Услуга "${serviceData.title}" уже существует, пропускаем...`);
        continue;
      }

      const service = await prisma.service.create({
        data: serviceData,
      });

      console.log(`✅ Услуга создана: ${service.title}`);
      console.log(`   ID: ${service.id}`);
      console.log(`   Статус: ${service.status}\n`);
    }

    console.log('✨ Все услуги успешно добавлены в базу данных!');
  } catch (error) {
    console.error('❌ Ошибка при создании услуг:', error);
  } finally {
    await prisma.$disconnect();
  }
}

createServices();

